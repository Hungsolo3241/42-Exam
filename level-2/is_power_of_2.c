#include <unistd.h>

int		is_power_of_2(unsigned int n)
{
	int	is_power_of_2 = 1;

	while (is_power_of_2 < n && is_power_of_2 < 2147483648)
		is_power_of_2 *= 2;
	return (n == is_power_of_2);
}
